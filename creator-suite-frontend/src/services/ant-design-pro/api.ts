export async function login(body: API.LoginParams, options?: { [key: string]: any }) {
  // Convert the body data to URL encoded format
  const formData = new URLSearchParams();
  // Backend expects 'username' field but uses it for email
  const emailValue = body.email || body.username;
  formData.append('username', emailValue?.trim().toLowerCase() || '');
  formData.append('password', body.password || '');
  
  return request<API.LoginResult>('/api/v1/auth/login', {
    method: 'POST',
    headers: {
      'Content-Type': 'application/x-www-form-urlencoded',
    },
    data: formData.toString(),
    ...(options || {}),
  });
}

